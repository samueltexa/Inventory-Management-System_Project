    /**
      This method selects customer name from the database and adds it to the district comboBox
     */
    public void AddDataToCustomerName(){
        // Connect to the database and execute a query to add suppliers:
        try {
            Class.forName("com.mysql.jdbc.Driver"); //register the driver
            con = DriverManager.getConnection(cs, user, password);
            query = "SELECT Name FROM customer";
            st = con.createStatement();           
            statement = con.prepareStatement(query);
            rs = st.executeQuery(query);
             // Iterate over the result set and add items to the comboBox
            while(rs.next()){
                boxdelete.addItem(rs.getString("Name"));   
            }
             boxdelete.setSelectedItem(null);
             txtname.setText(null);
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(DeleteCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            try {
                con.close();
                statement.close();
                rs.close();
            } catch (SQLException ex) {
                Logger.getLogger(DeleteCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
